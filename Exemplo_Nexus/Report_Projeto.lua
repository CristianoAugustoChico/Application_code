	Descricao = "Os biomas <b>Cerrado e Caatinga</b> possuem os principais estoques de terras disponíveis para expansão agrícola no Brasil, além de áreas de elevado potencial solar e eólico. Seus recursos naturais são essenciais para a regulação do clima e bem-estar humano.<br>O objetivo geral do projeto NEXUS é propor estratégias que permitam a transição para um futuro sustentável nesses dois biomas, através de uma abordagem participativa capaz de integrar métodos qualitativos e quantitativos das ciências naturais e sociais em múltiplas escalas. Para tanto, serão definidos cenários e indicadores que conciliem as dimensões econômica, social e ambiental (os três pilares da sustentabilidade) na produção agrícola e energética, bem como no uso de recursos naturais."

.."<h2>Etapas do Projeto</h2>"

.."<p>O NEXUS está organizado em três etapas transversais no desenvolvimento da pesquisa.</p>"

.."<p>A <b>Etapa 1</b> produzirá indicadores sobre as dimensões social, econômica, institucional e ambiental da área de estudo, visando à construção de índices de sustentabilidade que reflitam a situação atual (passado e presente) em alinhamento com as demandas nacionais.</p>"

.."<p>A <b>Etapa 2</b> abordará o futuro da área de estudo, em um processo participativo de construção de cenários qualitativos e quantitativos. Para isso, serão geradas projeções espacialmente explícitas de mudanças de uso da terra, do clima regional e seus impactos.</p>"

.."<p>A <b>Etapa 3</b> englobará atividades de síntese entre as escalas, assim como de análise de mecanismos de transformação social, visando apresentar caminhos que levem à transição para a sustentabilidade.</p>",

